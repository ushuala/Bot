function module:move()
	pathSelection()
	if banque then
		return bank()
	end
	if not triggerHavenBag then
		triggerHavenBag = true
	end
	for x,y in pairs(selectedPath) do
		if y.map == map:currentMap() or y.map == tostring(map:currentMapId()) then
			return selectedPath
		end
	end
	return {
		{map = map:currentMap(), path = "havenbag"},
	}
end

function module:bank()
	banque = true
	if map:currentMapId() ~= 162791424 and map:currentMap() ~= "-32,-56" and map:currentMap() ~= "-32,-55" and map:currentMap() ~= "-32,-54" and map:currentMapId() ~= 147254 and map:currentMapId() ~= 2885641 then
		return { {map = tostring(map:currentMap()), path = "havenbag"}, }
	end
	return {
		{map = "162791424", path = "zaap(147768)"},
		{map = "-32,-56", path = "zaapi(147254)"},
		{map = "147254", door = "383"},
    {map = "2885641", custom = npcBank},
}
end



