function pathSelection()

    if (job:level(28) < 20) then

		GATHER = {38}

        selectedPath = firstArea

    elseif (job:level(28) >= 20) and (job:level(28) < 40) then

		GATHER = {38, 43}

        selectedPath = secondArea

    elseif (job:level(28) >= 40) and (job:level(28) < 60) then

		GATHER = {38, 43, 45}

        selectedPath = thirdArea

    elseif (job:level(28) >= 60) and (job:level(28) < 80) then

		GATHER = {39, 43, 45, 44}

    	selectedPath = forthArea

    elseif (job:level(28) >= 80) and (job:level(28) < 120) then

		GATHER = {39, 42, 45, 43, 44}

    	selectedPath = fifthArea

    elseif (job:level(28) >= 120) and (job:level(28) < 140) then

		GATHER = {39, 42, 45, 47, 43}

    	selectedPath = sixthArea

    elseif (job:level(28) >= 140) and (job:level(28) <= 200) then

		GATHER = {39, 42, 45, 47, 46, 43}

		selectedPath = seventhArea

    end

end



function move()

	wT()

	serverChecker()

	pathSelection()

	if banque then

		return bank()

	end

	if not triggerHavenBag then

		triggerHavenBag = true

	end

	for x,y in pairs(selectedPath) do

		if y.map == map:currentMap() or y.map == tostring(map:currentMapId()) then

			return selectedPath

		end

	end

	return {

		{map = map:currentMap(), path = "havenbag"},

	}

end



welT = true



function wT()

	if welT then

		global:printMessage("Pour que je puisse fonctionner sans encombre, merci de posséder les Zaaps : ...")

		global:delay(500)

		global:printMessage("- Bonta")

		welT = false

	end

end



function bank()

	serverChecker()

	banque = true

	if map:currentMapId() ~= 162791424 and map:currentMap() ~= "-32,-56" and map:currentMap() ~= "-32,-55" and map:currentMap() ~= "-32,-54" and map:currentMapId() ~= 147254 and map:currentMapId() ~= 2885641 then

		return { {map = tostring(map:currentMap()), path = "havenbag"}, }

	end

	return {

		{map = "162791424", path = "zaap(147768)"},

		{map = "-32,-56", path = "zaapi(147254)"},

		{map = "147254", door = "383"},

        {map = "2885641", custom = npcBank},

    }

end



function npcBank()

    npc:npcBank(-1)

    global:delay(1000)

    exchange:putAllItems()

    global:delay(1000)

